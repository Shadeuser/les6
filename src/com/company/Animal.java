package com.company;

public class Animal {

    public Animal() {

    }

    public void run(int distance) {
        System.out.println("Animal runs " + distance + " метров");
    }

    public void swim(int distance) {
        System.out.println("Animal swims " + distance + " метров");
    }

    public void jump(double height) {
        System.out.println("Animal jumps " + height + " метров");
    }

}
